/* photoCategoryButton 1 Sake */

function CB1C() { // 1_アイコン画像に切り替え
  document.getElementById('CBa1').innerHTML = '<img src=\"./image/Sake.png\" class=\"CategoryButtonLogo\">';
  document.getElementById('CBa1').style.width = '78px'; //solidで囲う分マイナス2pxして調整
  document.getElementById('CBa1').style.height = '18px'; //solidで囲う分マイナス2pxして調整
  document.getElementById('CBa1').style.backgroundColor = '#FFFFFF';
  document.getElementById('CBa1').style.borderStyle = 'solid';
  document.getElementById('CBa1').style.borderWidth = '1px';
  document.getElementById('CBa1').style.borderColor = '#4c4c4c';
  document.getElementById('topPhoto').innerHTML = '<img src=\"./image/Sake.jpg\" class=\"categoryPhoto\">';
};

function CB1R() { // 1_文字に戻し
  document.getElementById('CBa1').innerHTML = 'Sake!';
  document.getElementById('CBa1').style.width = '80px';
  document.getElementById('CBa1').style.height = '20px';
  document.getElementById('CBa1').style.backgroundColor = '#4c4c4c'
  document.getElementById('CBa1').style.borderStyle = '';
  document.getElementById('topPhoto').innerHTML = '<div id=\"topPhoto\">Photo!</div>';
};

